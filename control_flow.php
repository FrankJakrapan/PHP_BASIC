<div>IF Else</div>

<?php
$x = 10;
if($x > 5){
    echo "x is greater than 5";
}else{
    echo "x is less than 5";
}
?>


<div>Switch</div>

<?php
switch($x){
    case 5:
        echo "x is 10";
        break;
    case 20:
        echo "x is 20";
        break;
    default:
        echo "x is not 10 or 20";
        break;
}
?>